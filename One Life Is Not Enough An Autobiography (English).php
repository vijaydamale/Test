Natwar Singh joined the Indian Foreign Service and served as a bureaucrat for 31 years. He joined the Congress Party in 1984, and became a Minister of State in the then Prime Minister Rajiv Gandhi’s council with the portfolios of steel, agriculture, and coal and mines in 1985. In this much-awaited autobiography, the former cabinet minister talks justly about his experiences and services in various ministries. Singh has played a significant role in Indian politics for more than twenty years and has been a part of some of the most epochal events of independent India, including Indo-China talks and the formation of Bangladesh. In 2004, when the Congress party came back to power, Natwar Singh was appointed as the Minister for External Affairs. But his eventful career saw its end with the Volcker Report in the year 2005. His name appearing in the Iraqi food-for-oil scam forced him to resign from the cabinet and eventually from the Congress party. Singh talks about all these events and the ups and downs of the Congress party in One Life Is Not Enough, an account of an insider. His association with the party allowed him to observe some of the historical events closely, and he talks about Pakistan in the 1980s, under the rule of President Zia-ul-Haq, Indo-Chinese and Indo-USSR relations among other sensitive developments.<br>

